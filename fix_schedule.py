def main():
    with open('schedule.json', 'r', encoding='utf-8') as f:
        old_schedule = json.load(f)
    
    print(f'Current entries: {len(old_schedule)}')
    print(f'FIFA entries: {len(fifa_schedule)}')
    
    # Match by team pair only (order-insensitive), ignoring date
    new_schedule = []
    unmatched = []
    used_fifa = set()
    
    for old in old_schedule:
        old_teams = frozenset([old['team_a'], old['team_b']])
        
        found = None
        for i, f in enumerate(fifa_schedule):
            if i in used_fifa:
                continue
            f_teams = frozenset([f[2], f[3]])
            if old_teams == f_teams:
                found = f
                used_fifa.add(i)
                break
        
        if found:
            # Use old team order, FIFA date+time
            new_schedule.append({
                'date': found[0],
                'team_a': old['team_a'],
                'team_b': old['team_b'],
                'group': old['group'],
                'round': found[5],
                'time_bj': found[1]
            })
        else:
            unmatched.append(old)
            new_schedule.append(old)
    
    print(f'\nMatched: {len(new_schedule) - len(unmatched)}')
    print(f'Unmatched: {len(unmatched)}')
    
    if unmatched:
        print('=== UNMATCHED ===')
        for u in unmatched:
            print(f'  {u["date"]} {u["time_bj"]} | {u["team_a"]} vs {u["team_b"]}')
    
    # Show all changes (date or time)
    print('\n=== ALL CHANGES (date + time) ===')
    for old, new in zip(old_schedule, new_schedule):
        if (old['date'] != new['date']) or (old['time_bj'] != new['time_bj']):
            print(f'  {old["team_a"]} vs {old["team_b"]}')
            print(f'    date: {old["date"]} -> {new["date"]}')
            print(f'    time: {old["time_bj"]} -> {new["time_bj"]}')
    
    # Sort by date + time
    new_schedule.sort(key=lambda x: (x['date'], x['time_bj']))
    
    with open('schedule.json', 'w', encoding='utf-8') as f:
        json.dump(new_schedule, f, ensure_ascii=False, indent=2)
    
    print(f'\nWrote {len(new_schedule)} sorted entries to schedule.json')

if __name__ == '__main__':
    main()
